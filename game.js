		function getMinutosSegundos(timer) {
			let minutos = parseInt((timer / self.fps) / 60);
			let segundos = parseInt((timer / self.fps) % 60);
			minutos = minutos < 10 ? '0' + minutos : minutos;
			segundos = segundos < 10 ? '0' + segundos : segundos;	
			return [minutos, segundos, minutos + ':' + segundos];
		}
		
		function alinhaTextoBox(texto, box) {
			let offset = 3;
			let textoAltura = texto.getMeasuredHeight();
			texto.y = box.y + (box.altura/2) - (textoAltura/2) + offset;
		}
		
